/**
 * Get color info for an ARK Color ID.
 * @param {number} id - Color ID (0–255)
 * @returns {{ name: string, hex: string|null }}
 */
function getColor(id) {
  if (id === 0 || id == null) return { name: 'Unset', hex: null };
  return ARK_COLORS[id] || { name: `Unknown (${id})`, hex: '#888888' };
}

/**
 * Convert a hex color string to { r, g, b } object.
 * @param {string} hex
 * @returns {{ r: number, g: number, b: number }}
 */
function hexToRgb(hex) {
  if (!hex) return { r: 128, g: 128, b: 128 };
  const result = /^#?([a-f\d]{2})([a-f\d]{2})([a-f\d]{2})$/i.exec(hex);
  return result
    ? { r: parseInt(result[1], 16), g: parseInt(result[2], 16), b: parseInt(result[3], 16) }
    : { r: 128, g: 128, b: 128 };
}

